// Initialize the website with the stored language preference or default
document.addEventListener('DOMContentLoaded', function () {
    // Check if there's a stored language preference
    const storedLanguage = localStorage.getItem('viharaLanguage');

    if (storedLanguage) {
        // Use the stored language preference
        translateWebsite(storedLanguage);
    } else {
        // Use default language (Indonesian)
        translateWebsite('id');
    }

    // Add click event listeners to language toggle buttons
    const idButton = document.querySelector('a[title="Bahasa Indonesia"]');
    const enButton = document.querySelector('a[title="English"]');

    if (idButton) {
        idButton.addEventListener('click', translateToIndonesian);
    }

    if (enButton) {
        enButton.addEventListener('click', translateToEnglish);
    }
});
