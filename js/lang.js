// Keep track of current language
let currentLanguage = 'id'; // Default language is Indonesian

// Function to translate the website to the selected language
function translateWebsite(language) {
    currentLanguage = language;

    // Save language preference to localStorage for persistence
    localStorage.setItem('viharaLanguage', language);

    // Translate navigation menu
    document.querySelectorAll('.s-header__menu-links li a').forEach(item => {
        const key = item.getAttribute('href').replace('#', '');
        if (translations.nav[language][key]) {
            item.textContent = translations.nav[language][key];
        }
    });

    // Translate mobile menu toggle
    const menuToggle = document.querySelector('.s-header__menu-toggle span');
    if (menuToggle) {
        menuToggle.textContent = translations.nav[language]['beranda'];
    }

    // Translate intro section
    const introTitle = document.querySelector('.s-intro__content-title');
    if (introTitle) {
        introTitle.innerHTML = translations.intro[language]['title'];
    }

    const introBtn = document.querySelector('.s-intro__content-btn');
    if (introBtn) {
        introBtn.textContent = translations.intro[language]['button'];
    }

    const scrollDown = document.querySelector('.s-intro__scroll-down span');
    if (scrollDown) {
        scrollDown.textContent = translations.intro[language]['scroll'];
    }

    // Translate sejarah (history) section
    const sejarahPretitle = document.querySelector('#sejarah .wg-pretitle');
    if (sejarahPretitle) {
        sejarahPretitle.textContent = translations.sejarah[language]['sectionTitle'];
    }

    const sejarahTitle = document.querySelector('#sejarah .wg-header-title h2');
    if (sejarahTitle) {
        sejarahTitle.textContent = translations.sejarah[language]['mainTitle'];
    }

    const sejarahDesc = document.querySelector('#sejarah .wg-header-desc h6');
    if (sejarahDesc) {
        sejarahDesc.textContent = translations.sejarah[language]['description'];
    }

    // Translate timeline items
    const timelineItems = document.querySelectorAll('.wg-timeline-content h3');
    if (timelineItems.length > 0) {
        // Awal Mula
        if (timelineItems[0]) {
            timelineItems[0].textContent = translations.sejarah[language]['awalMula'];
            const awalMulaText = timelineItems[0].nextElementSibling.nextElementSibling;
            if (awalMulaText) {
                awalMulaText.textContent = translations.sejarah[language]['awalMulaText'];
            }
        }

        // Nama Watugong
        if (timelineItems[1]) {
            timelineItems[1].textContent = translations.sejarah[language]['namaWatugong'];
            const namaWatugongText = timelineItems[1].nextElementSibling.nextElementSibling;
            if (namaWatugongText) {
                namaWatugongText.textContent = translations.sejarah[language]['namaWatugongText'];
            }
        }

        // Perkembangan Modern
        if (timelineItems[2]) {
            timelineItems[2].textContent = translations.sejarah[language]['modern'];
            const modernText = timelineItems[2].nextElementSibling.nextElementSibling;
            if (modernText) {
                modernText.textContent = translations.sejarah[language]['modernText'];
            }
        }
    }

    // Translate feature items in sejarah section
    const featureItems = document.querySelectorAll('.wg-feature-item');
    if (featureItems.length >= 4) {
        // Warisan Budaya
        const warisan = featureItems[0].querySelector('h3');
        const warisanText = featureItems[0].querySelector('p');
        if (warisan && warisanText) {
            warisan.textContent = translations.sejarah[language]['warisanTitle'];
            warisanText.textContent = translations.sejarah[language]['warisanText'];
        }

        // Tempat Spiritual
        const spiritual = featureItems[1].querySelector('h3');
        const spiritualText = featureItems[1].querySelector('p');
        if (spiritual && spiritualText) {
            spiritual.textContent = translations.sejarah[language]['spiritualTitle'];
            spiritualText.textContent = translations.sejarah[language]['spiritualText'];
        }

        // Arsitektur Menawan
        const arsitektur = featureItems[2].querySelector('h3');
        const arsitekturText = featureItems[2].querySelector('p');
        if (arsitektur && arsitekturText) {
            arsitektur.textContent = translations.sejarah[language]['arsitekturTitle'];
            arsitekturText.textContent = translations.sejarah[language]['arsitekturText'];
        }

        // Destinasi Wisata
        const wisata = featureItems[3].querySelector('h3');
        const wisataText = featureItems[3].querySelector('p');
        if (wisata && wisataText) {
            wisata.textContent = translations.sejarah[language]['wisataTitle'];
            wisataText.textContent = translations.sejarah[language]['wisataText'];
        }
    }

    // Translate fasilitas (facilities) section
    const fasilitasPretitle = document.querySelector('#fasilitas .vw-pretitle');
    if (fasilitasPretitle) {
        fasilitasPretitle.textContent = translations.fasilitas[language]['sectionTitle'];
    }

    const fasilitasTitle = document.querySelector('#fasilitas .vw-title');
    if (fasilitasTitle) {
        fasilitasTitle.textContent = translations.fasilitas[language]['Title'];
    }
}

